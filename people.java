    private void jLabel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel2MouseClicked
        // TODO add your handling code here:
        String sql="SELECT p.people_no , p.pepname as Magaca ,p.tell ,p.sex as jinsi,p.birth_date , p.weight ,j.jobname as magacashaqada,"
                + "p.description ,p.reg_date FROM people  p , jobs j "
                + "WHERE p.job_no=j.job_no and people_no='"+txtpeopleSer.getText()+"'";
     co.search(sql);
     try{
         if(co.rs.next()){
             idpep=co.rs.getString("people_no");
             txtpeopleName.setText(co.rs.getString("Magaca"));
             txtpeopleTell.setText(co.rs.getString("tell"));
             Gender=co.rs.getString("jinsi");
             if(Gender.equals("Male"))
                 male.setSelected(true);
             else if(Gender.equals("Female"))
                 female.setSelected(true);
             jDatepeople.setDate(co.rs.getDate("birth_date"));
             txtweipeople.setText(co.rs.getString("weight"));
             txtdespeople.setText(co.rs.getString("description"));
             jDatepeeoplereg.setDate(co.rs.getDate("reg_date"));
             peoplejob_no.setSelectedItem(co.rs.getString("magacashaqada"));
             
             btnsave.setEnabled(false);
             txtpeopleSer.setText("");
         }
         else
             JOptionPane.showMessageDialog(null, "Xogtaan Lama Helin");
            txtpeopleSer.setText("");
     }catch(Exception ex){
         JOptionPane.showMessageDialog(null, ex.getMessage());
     }
    }//GEN-LAST:event_jLabel2MouseClicked

