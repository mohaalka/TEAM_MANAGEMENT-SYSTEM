    private void txtpeopleTellKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtpeopleTellKeyPressed
         // TODO add your handling code here:
         char c= evt.getKeyChar();
        if(Character.isDigit(c)|| Character.isWhitespace(c)|| Character.isISOControl(c)){
            txtpeopleTell.setEditable(true);
            showtell.setText("");
        }
        else
        {
            txtpeopleTell.setEditable(false);
            showtell.setText("Only Digits Allowed");
        }
         
         
    }//GEN-LAST:event_txtpeopleTellKeyPressed

    private void txtdespeopleKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtdespeopleKeyPressed
        // TODO add your handling code here:
        char c= evt.getKeyChar();
        if(Character.isLetter(c)|| Character.isWhitespace(c)|| Character.isISOControl(c)){
            txtdespeople.setEditable(true);
            showdesc.setText("");
        }
        else
        {
            txtdespeople.setEditable(false);
            showdesc.setText("Only Letters Are Allowed");
        }
        
        
        
    }//GEN-LAST:event_txtdespeopleKeyPressed

